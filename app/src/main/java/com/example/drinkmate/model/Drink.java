    public List<String> getIngredientsWithMeasures() {
        List<String> result = new ArrayList<>();
        String[] ingredients = {
            strIngredient1, strIngredient2, strIngredient3, strIngredient4, strIngredient5, 
            strIngredient6, strIngredient7, strIngredient8, strIngredient9, strIngredient10, 
            strIngredient11, strIngredient12, strIngredient13, strIngredient14, strIngredient15
        };
        String[] measures = {
            strMeasure1, strMeasure2, strMeasure3, strMeasure4, strMeasure5, 
            strMeasure6, strMeasure7, strMeasure8, strMeasure9, strMeasure10, 
            strMeasure11, strMeasure12, strMeasure13, strMeasure14, strMeasure15
        };

        for (int i = 0; i < ingredients.length; i++) {
            if (ingredients[i] != null && !ingredients[i].trim().isEmpty()) {
                String ingredient = ingredients[i].trim();
                String measure = (measures[i] != null) ? measures[i].trim() : "";
                if (!measure.isEmpty()) {
                    result.add(measure + " " + ingredient);
                } else {
                    result.add(ingredient);
                }
            } else {
                // Stop if ingredient is null or empty, assuming no more ingredients follow
                break; 
            }
        }
        return result;
    }
